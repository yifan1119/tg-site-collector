def get_category(name: str) -> list[str]:
    """按类别取关键词。未知类别返 []。"""
    return list(CATEGORIES.get(name, []))


def all_keywords() -> list[str]:
    """全量 4 类合并 · 去重。"""
    seen: set[str] = set()
    out: list[str] = []
    for kws in CATEGORIES.values():
        for kw in kws:
            if kw not in seen:
                seen.add(kw)
                out.append(kw)
    return out


def template_id(category: str) -> str:
    """模板稳定 id · 给 trigger API 用。"""
    return f"tpl_{category}"


def build_template_lists() -> list[dict[str, Any]]:
    """build 所有平台模板（is_template=true）· seed 用。"""
    out = [
        {
            "id": template_id(cat),
            "name": f"{cat}站（平台模板）",
            "description": f"{cat}类成人站点关键词，共 {len(words)} 个词",
            "keywords": list(words),
            "is_template": True,
            "category": cat,
        }
        for cat, words in CATEGORIES.items()
    ]
    full = all_keywords()
    out.append(
        {
            "id": "tpl_全量",
            "name": "全量（平台模板）",
            "description": f"4 类合并去重，共 {len(full)} 个词",
            "keywords": full,
            "is_template": True,
            "category": "全量",
        }
    )
    return out
